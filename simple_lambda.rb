puts -> (x, y) {x * y + 1}.call(6, 2)

# Kode diatas menggunakan lambda atau Proc dalam bahasa Ruby. Lambda tersebut dibuat dengan sintaks -> (x, y) dan memiliki blok kode {x * y + 1}. Melalui pemanggilan .call(6, 2), lambda tersebut dieksekusi dengan memberikan nilai 6 untuk x dan 2 untuk y. Hasilnya, yang adalah (6 * 2) + 1 = 13, ditampilkan ke konsol menggunakan fungsi puts.
